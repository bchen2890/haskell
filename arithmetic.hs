main = do
    putStrLn "2 + 1 ="
    print $ 2 + 1
    putStrLn "5 - 3 ="
    print $ 5 - 3
    putStrLn "4 * 5 ="
    print $ 4 * 5
    putStrLn "22 / 2 ="
    print $ 22 / 2
    putStrLn "(30/6) + 4 - (2 * 3) ="
    print $ (30/6) + 4 - (2 * 3)
    putStrLn "succ 9 ="
    print $ succ 9
    putStrLn "min -2 1 ="
    print $ min (-2) 1  
    putStrLn "max 5 9 ="
    print $ max 5 9